public class Primitive {
    public static void main(String[] args) {
        int integer = 10;
        char letter = 'R';
        float decimal = 2.234f;
        double largerDecimal = 2.4567983452734536;
        long largeInteger = 2345678990342123421l;
        boolean check = false;
        byte veryShortNumber = -128; //-128 to +127
        short shortNumber = -32768;//-32768 to 32767

        System.out.println(integer);
        System.out.println(letter);
        System.out.println(decimal);
        System.out.println(largerDecimal);
        System.out.println(largeInteger);
        System.out.println(check);
        System.out.println(veryShortNumber);
        System.out.println(shortNumber);


    }
}
